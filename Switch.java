class Switch{

    public static void main(String[] args) {
        


        int a=13;
        // int float char String
       

        switch(a){
            case 1: System.out.println("Case 1 JAN ");
            break;

            case 2: System.out.println("Case 2 FEB");
            break;
          
            case 3: System.out.println("Case 3 MARCH");
            break;

            default: System.out.println("Invalid input");
        }


        char b='A';
        // int float char String

        switch(b){
            case 'B': System.out.println("Case 1 JAN ");
            

            case 'A': System.out.println("Case 2 FEB");
            break;
          
            case 'C': System.out.println("Case 3 MARCH");
            break;

            default: System.out.println("Invalid input");
        }
    }
}